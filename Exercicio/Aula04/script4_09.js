let a,b;
a=10; //10 numero inteiro
b="10";//10 string texto
document.write("Igualdade =" +(a==b)+"<br>"); //true - o valor 10 é igual a string 10 - valores iguais
document.write("Identico=" +(a===b)+"<br>"); // false - variavel A não é identico a String A, Por que String A é um texto
document.write("Não igual=" +(a!=b)+"<br>");// false - Não é igual
document.write("Não Identico =" +(a!==b)+"<br>");//True não identico, 

/*
= Atribuição/receber
== igualdade - valor
=== identico - faz comparação do valor e tipo
!= não igual  (Diferente) - eles são diferente
!== Não Identico
*/
